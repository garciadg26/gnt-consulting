                                    <h5 class="azul">¿Qué es una fianza?</h5>
                                    <p>Una fianza es un contrato en el que un fiador (la compañía de fianzas) garantiza el cumplimiento de una obligación por parte del afianzado (la persona o empresa que necesita la fianza).</p>

                                    <h5 class="azul">¿Qué tipos de fianzas existen?</h5>
                                    <p>Existen diferentes tipos de fianzas, incluyendo fianzas de cumplimiento, de anticipo, de garantía y de crédito.</p>

                                    <h5 class="azul">¿Quiénes pueden obtener una fianza?</h5>
                                    <p>Las personas físicas y jurídicas pueden obtener una fianza, siempre y cuando cumplan con los requisitos establecidos por la compañía de fianzas.</p>

                                    <h5 class="azul">¿Cómo se obtiene una fianza?</h5>
                                    <p>Para obtener una fianza, se debe presentar una solicitud a una compañía de fianzas, proporcionando información sobre la obligación que se desea garantizar, así como sobre el afianzado. La compañía de fianzas evaluará el riesgo y establecerá las condiciones y la prima correspondiente.</p>

                                    <h5 class="azul">¿Cuál es el costo de una fianza?</h5>
                                    <p>El costo de una fianza varía dependiendo del tipo de obligación que se desea garantizar, así como del riesgo que representa el afianzado. La prima se establece en base a una tasa, que suele ser un porcentaje del monto de la fianza.</p>

                                    <h5 class="azul">¿Qué sucede si no se cumple con la obligación garantizada por la fianza?</h5>
                                    <p>Si el afianzado no cumple con la obligación garantizada por la fianza, la compañía de fianzas pagará la cantidad garantizada a la parte perjudicada, y luego se subrogará en los derechos de la parte perjudicada para recuperar el monto de la fianza.</p>

                                    <h5 class="azul">¿Cuánto tiempo dura una fianza?</h5>
                                    <p>La duración de una fianza depende del tipo de obligación que se desea garantizar y de las condiciones establecidas por la compañía de fianzas. En general, las fianzas de cumplimiento suelen tener una duración determinada, mientras que las fianzas de crédito pueden tener una duración indefinida.</p>
